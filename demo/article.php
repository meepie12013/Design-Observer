<h1 style="font-size:4rem;">Adrian Shaughnessy</h1>
<h2>An Interview with Neville Brody</h2>
                        <img src="../img/likelove.jpg" width="200px;"><br>
                        <div><em>As part of a week of posts celebrating the GraphicsRCA: Fifty Years exhibition, Adrian Shaughnessy interviews the RCA&rsquo;s Dean of the School of Communication and designer Neville Brody.<br/><br/>&nbsp;</em></div>
<div><strong>Adrian Shaughnessy</strong>: In your view, what are the unique and defining characteristics of the <a href="http://www.rca.ac.uk/">RCA</a>?</div>
<div>&nbsp;</div>
<div><strong>Neville Brody</strong>: The RCA can be defined by its resistance to being defined. It operates as a laboratory for developing skilled, dangerous minds&ndash;minds that will lead and change society and industry. It is also highly collaborative, based on a principle of developing cross-disciplinary practice. Many students work across departments and skills, graduating into a world that increasingly demands such lateral capability and thought. The RCA is small, has a teaching ratio of 15:1, is solely postgraduate, and at over 175 years, is the oldest in continual existence art and design school in the world.</div>
<div>&nbsp;</div>
<div><strong>AS</strong>: Since the 1980s you have been at the forefront of graphic design practice, yet up until arriving at the RCA five years ago, you had no teaching experience. Today you are <a href="http://www.rca.ac.uk/more/staff/professor-neville-brody/">Dean of the School of Communication</a>, what do you bring to the role that someone with more experience in education might not bring?</div>
<div>&nbsp;</div>
<div><strong>NB</strong>: What I lack in academic experience, I hope I make up for in vision and strategic thinking. Whilst needing to comply with a large amount of administrative deliverables and meetings, I spend as much time as I can looking at ways of moving into new and relevant spaces.&nbsp;Teaching in an elective titled Design Without, which operates as a kind of de-programming boot camp, allows me to explore, invent, and seek out new mechanisms to extend to the wider curriculum, as well as performing the vital role of helping me keep in touch with students.</div>
<div>&nbsp;</div>
<div><strong>AS</strong>: In your role as Dean you are involved in developing and growing a <a href="http://www.rca.ac.uk/research-innovation/">research culture at the College</a>&ndash;can you say something about this in light of the fact that for many graphic designers, research (in the academic sense) and practice are not always seen as compatible?</div>
<div>&nbsp;</div>
<div><strong>NB</strong>: Research belongs at the heart of both graphic design and the educational process, and we embrace it as a way to see, inform and change the world. Graphic design, in its role as observer and reflector, necessarily incorporates and operates from a contextual and critical position. Graphic design, as with all forms of communication, is persuasive. How it is done, why it is done, who it is done for, and for what outcome, are all intrinsic questions, and as such necessitate both a critical and self-critical approach.</div>
<div>&nbsp;</div>
<div>The research culture at the College, especially through collaborative projects like CX (<a href="http://thecreativeexchange.org/">The Creative Exchange</a>), sets out to explore social design through innovative practice. The conflict at the heart of graphic design between that of persuasion and enablement continues to demand such mechanisms.</div>
<div>&nbsp;</div>
<div><strong>AS</strong>: We are currently looking back at <a href="http://graphics50.rca.ac.uk/#home">fifty years of graphic design</a> at the RCA. How would you characterize the past half-century at the RCA, and what is your vision for the future?</div>
<div>&nbsp;</div>
<div><strong>NB</strong>: What was recognized fifty years ago as a radical craft, perfectly placed to engineer change in society through conscious intervention, is once again how we are beginning to see graphic design, as we did in the seventies, eighties and at some point in the nineties. This speeding oscillation produces great individuals and ideas at the turning of the circle, and throws them off, often at wild tangents. Graphic design is always informed and enabled by technology, to which it has a strong affinity and relationship. As a means for making and distributing change, so graphic design morphs again. There will always be a relevant role and function for graphic design, especially at the RCA, and the skills necessary that underpin its industry potentially stand as both a voice of reason and reasoning, and a mechanism for change, which is the only constant I can imagine for its future.</div>
<div>&nbsp;</div>
<div><strong>AS</strong>: How has the experience of immersing yourself in academia changed you as an individual and as a practitioner?&nbsp;</div>
<div>&nbsp;</div>
<div><strong>NB</strong>: My confidence has certainly grown in the area of academia, honed by the enormous pressure felt by everyone involved in education today. The Higher Education landscape particularly has become virtually unrecognizable through the disruption caused by unjust and debilitating higher fees, a dramatically shifting reductive academic criteria based on an uninformed view of relevance (<a href="http://www.ed.gov/stem">STEM</a> anyone?), increased numbers, financial insecurity, the stress of greater staff performance accountability, and a new emphasis on industry partnerships.</div>
<div>&nbsp;</div>
<div>In many ways it has helped me mature as a practitioner. I appreciate a wider landscape and have gained a deeper understanding, but equally, I yearn for the opposite and I&rsquo;m realising again how important it is to disobey. I sense the need for radical, unprecious and disruptive design again, and feel myself being drawn anew towards unstructured chance and structured mayhem.</div>
<div></div> <br/><br/>
